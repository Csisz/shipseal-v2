function ciWorkflow(report: ReadinessReport, isExample = false) {
  const install = installCommand(report);
  const test = commandFor(report, 'test') || 'npm test';
  const build = commandFor(report, 'build') || 'npm run build';
  const lint = commandFor(report, 'lint');

  return [
    ...(isExample ? [
      '# ShipSeal CI quality gate example',
      '# Documentation only: review before copying this file into .github/workflows/ci.yml.',
      '',
    ] : []),
    'name: ShipSeal Readiness CI',
    '# Generated by ShipSeal',
    '# Review before enabling in production.',
    '',
    'on:',
    '  pull_request:',
    '  push:',
    '    branches: [main]',
    '',
    'jobs:',
    '  quality-gate:',
    '    runs-on: ubuntu-latest',
    '    steps:',
    '      - name: Checkout',
    '        uses: actions/checkout@v4',
    '',
    '      - name: Setup Node',
    '        uses: actions/setup-node@v4',
    '        with:',
    '          node-version: 20',
    '          cache: npm',
    '',
    '      - name: Install dependencies',
    `        run: ${install}`,
    '',
    '      - name: Run tests',
    `        run: ${test}`,
    '',
    ...(lint ? ['      - name: Run lint', `        run: ${lint}`, ''] : []),
    '      - name: Run build',
    `        run: ${build}`,
  ].join('\n');
}

function genericMarkdown(report: ReadinessReport, entry: ReadinessFixPackManifestEntry) {
  return markdown(report, entry, [
    `# ${entry.title}`,
    '',
    `Repository: ${report.repoName}`,
    'Generated by ShipSeal as part of the Readiness Fix Pack.',
    '',
    `## Purpose`,
    '',
    entry.purpose,
    '',
    `## Readiness Category`,
    '',
    entry.readinessCategory,
    '',
    `## Expected Impact`,
    '',
    entry.expectedImpact,
  ]);
}

function markdown(report: ReadinessReport, entry: ReadinessFixPackManifestEntry, lines: string[]) {
  return [
    ...lines,
    '',
    '## Fix Pack Metadata',
    '',
    `- Readiness category: ${entry.readinessCategory}`,
    `- Expected impact: ${entry.expectedImpact}`,
    `- Safe to add automatically: ${entry.isSafeToAdd ? 'yes, after review' : 'no'}`,
    `- Requires human review: ${entry.requiresHumanReview ? 'yes' : 'no'}`,
    `- Stack: ${stackLabel(report)}`,
    '',
    'Generated by ShipSeal.',
  ].join('\n');
}

function stackLabel(report: ReadinessReport) {
  return report.stack.primary || report.summary.detectedStack || 'Not detected';
}

function commandList(report: ReadinessReport) {
  const commands = recommendedCommands(report);
  if (!commands.length) return '- No build/test/lint commands were detected. Add project-specific commands before relying on this checklist.';
  return commands.map(command => `- \`${command}\``).join('\n');
}

function recommendedCommands(report: ReadinessReport) {
  const commands = [
    installCommand(report),
    commandFor(report, 'lint'),
    commandFor(report, 'test'),
    commandFor(report, 'build'),
  ].filter(Boolean) as string[];

  return [...new Set(commands)];
}

function installCommand(report: ReadinessReport) {
  const manager = report.summary.packageManager || report.stack.packageManagers[0] || 'npm';
  if (manager === 'pnpm') return 'pnpm install';
  if (manager === 'yarn') return 'yarn install';
  if (manager === 'bun') return 'bun install';
  return 'npm install';
}

function commandFor(report: ReadinessReport, scriptName: string) {
  if (!report.stack.scripts?.[scriptName]) return '';
  const manager = report.summary.packageManager || report.stack.packageManagers[0] || 'npm';
  if (manager === 'pnpm') return `pnpm ${scriptName}`;
  if (manager === 'yarn') return `yarn ${scriptName}`;
  if (manager === 'bun') return `bun run ${scriptName}`;
  return `npm run ${scriptName}`;
}
