import type { ReadinessReport } from '../types';
import { READINESS_FIX_PACK_MANIFEST } from './manifest';
import type { ReadinessFixPackFile, ReadinessFixPackManifestEntry } from './types';

export function buildSuggestedReadinessFixPack(report: ReadinessReport): ReadinessFixPackFile[] {
  const deliveryPackPaths = new Set([
    ...report.agentPack.map(file => file.name),
    ...report.mcpReadiness.generatedFiles.map(file => file.filename),
    '01-agent-instructions/AGENTS.md',
    '01-agent-instructions/CLAUDE.md',
    '04-testing/CI_QUALITY_GATE.yml',
    '06-client-handoff/CLIENT_HANDOFF_REPORT.md',
  ]);

  return READINESS_FIX_PACK_MANIFEST.map(entry => ({
    ...entry,
    improves: entry.purpose,
    whyUseful: entry.purpose,
    alreadyInDeliveryPack: deliveryPackPaths.has(entry.path) || deliveryPackPaths.has(entry.path.split('/').pop() || entry.path),
    content: buildReadinessFixPackFileContent(report, entry),
  }));
}

export function buildReadinessFixPackFileContent(report: ReadinessReport, entry: ReadinessFixPackManifestEntry) {
  switch (entry.path) {
    case 'AGENTS.md':
      return agentInstructions(report, entry, 'AGENTS.md');
    case 'CLAUDE.md':
      return agentInstructions(report, entry, 'CLAUDE.md');
    case 'CONTRIBUTING.md':
      return contributing(report, entry);
    case 'SECURITY.md':
      return security(report, entry);
    case 'docs/CRITICAL_FILES_POLICY.md':
      return criticalFilesPolicy(report, entry);
    case 'docs/RELEASE_CHECKLIST.md':
      return releaseChecklist(report, entry);
    case 'docs/OWNERSHIP.md':
      return ownership(report, entry);
    case 'docs/shipseal/CI_QUALITY_GATE.example.yml':
      return ciWorkflow(report, true);
    case 'docs/AGENT_CHANGE_POLICY.md':
      return agentChangePolicy(report, entry);
    case 'docs/HANDOFF_CHECKLIST.md':
      return handoffChecklist(report, entry);
    case 'docs/AI_ACT_READINESS_NOTES.md':
      return aiActReadinessNotes(report, entry);
    default:
      return genericMarkdown(report, entry);
  }
}

