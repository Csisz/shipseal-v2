    '',
    '## How AI Coding Agents Should Work Here',
    '',
    '- Read this file and the repository README before editing.',
    '- Prefer small, reviewable changes.',
    '- Do not execute unknown scripts or uploaded code outside the normal project commands.',
    '- Keep business logic, export structure and scanner behavior stable unless the task explicitly asks for changes.',
    '- Explain assumptions and verification results in the final handoff.',
    '',
    '## Recommended Commands',
    '',
    commandList(report),
    '',
    '## Files Requiring Human Review',
    '',
    '- Authentication, payment, security, legal, compliance and data handling code.',
    '- CI/CD, deployment, production configuration and environment variable handling.',
    '- Scanner limits, export manifests, generated report templates and client-facing claims.',
    '',
