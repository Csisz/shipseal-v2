export interface BuildAgentPackZipOptions {
  repositoryName?: string;
  scoreJson?: unknown;
  intake?: PartialProjectIntake;
}

export function sanitizeRepositoryName(name: string): string {
  const cleaned = name
    .trim()
    .replace(/\.zip$/i, '')
    .replace(/[^a-z0-9._-]+/gi, '-')
    .replace(/-+/g, '-')
    .replace(/^[-._]+|[-._]+$/g, '')
    .toLowerCase();

  return cleaned || 'repository';
}

export function buildAgentPackZipFilename(repositoryName: string): string {
  return `shipseal-delivery-pack-${sanitizeRepositoryName(repositoryName)}.zip`;
}

export function buildScoreJson(report: ReadinessReport): ScoreJsonExport {
  return {
    product: 'ShipSeal',
    generatedBy: 'Generated by ShipSeal',
    repositoryName: report.repoName,
    scanTimestamp: report.scannedAt,
    source: report.source,
    score: report.score,
    status: report.level,
    isReady: report.isReady,
    criticalBlockers: report.blockers,
    improvements: report.improvements,
    categories: report.categories,
    detectedStack: report.stack,
    scanSummary: report.scanSummary,
    aiNarrative: report.aiNarrative,
    repoContextPack: {
      repositoryName: report.repoContextPack.repositoryName,
      detectedStack: report.repoContextPack.detectedStack,
      packageManager: report.repoContextPack.packageManager,
      scriptCount: Object.keys(report.repoContextPack.scripts).length,
      keyFolders: report.repoContextPack.keyFolders,
      existingInstructionFiles: report.repoContextPack.existingInstructionFiles,
      blockerCount: report.repoContextPack.blockers.length,
      ignoredFolders: report.repoContextPack.ignoredFolders,
      securityFindingCount: report.repoContextPack.securityFindings.length,
      mcpStatus: report.repoContextPack.mcpSummary.status,
      rawFileContentsIncluded: false,
    },
    generatedFiles: report.agentPack.map(file => file.name),
    mcpReadiness: {
      score: report.mcpReadiness.score,
      status: report.mcpReadiness.status,
      summary: report.mcpReadiness.summary,
      aiNarrative: report.mcpReadiness.aiNarrative,
      recommendedServerCategories: report.mcpReadiness.recommendedServerCategories,
      riskFindings: report.mcpReadiness.riskFindings,
      generatedFiles: report.mcpReadiness.generatedFiles.map(file => file.filename),
    },
  };
}

export function downloadTextFile(filename: string, content: string, type = 'text/plain;charset=utf-8') {
  const blob = new Blob([content], { type });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  link.click();
  URL.revokeObjectURL(url);
}

export function downloadJsonFile(filename: string, value: unknown) {
  downloadTextFile(filename, `${JSON.stringify(value, null, 2)}\n`, 'application/json;charset=utf-8');
}

