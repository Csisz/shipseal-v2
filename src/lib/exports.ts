export async function buildAgentPackZipBlob(
  files: AgentPackFile[],
  mcpFiles: MCPPolicyFile[] = [],
  contextFiles?: { markdown: string; json: unknown },
  options: BuildAgentPackZipOptions = {}
): Promise<Blob> {
  const JSZip = (await import('jszip') as ZipModule).default;
  const zip = new JSZip();
  const deliveryFiles = buildDeliveryPackFiles({
    agentFiles: files,
    mcpFiles,
    contextFiles,
    repositoryName: options.repositoryName,
    scoreJson: options.scoreJson,
    intake: options.intake,
  });

  for (const file of deliveryFiles) {
    zip.file(file.path, file.content);
  }

  return zip.generateAsync({ type: 'blob' });
}

export async function downloadAgentPackZip(
  repositoryName: string,
  files: AgentPackFile[],
  mcpFiles: MCPPolicyFile[] = [],
  contextFiles?: { markdown: string; json: unknown },
  scoreJson?: unknown,
  intake?: PartialProjectIntake
) {
  const normalizedIntake = normalizeProjectIntake(intake, repositoryName);
  const blob = await buildAgentPackZipBlob(files, mcpFiles, contextFiles, { repositoryName, scoreJson, intake: normalizedIntake });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = buildAgentPackZipFilename(normalizedIntake.projectName);
  link.click();
  URL.revokeObjectURL(url);
}
