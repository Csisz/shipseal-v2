export function sanitizeRepositoryName(name: string): string {
  const cleaned = name
    .trim()
    .replace(/\.zip$/i, '')
    .replace(/[^a-z0-9._-]+/gi, '-')
    .replace(/-+/g, '-')
    .replace(/^[-._]+|[-._]+$/g, '')
    .toLowerCase();

  return cleaned || 'repository';
}

export function buildAgentPackZipFilename(repositoryName: string): string {
  return `shipseal-delivery-pack-${sanitizeRepositoryName(repositoryName)}.zip`;
}

export function buildScoreJson(report: ReadinessReport, options: BuildScoreJsonOptions = {}): ScoreJsonExport {
  const folderAgentPaths = getFolderAgentSuggestionPaths(report.repoContextPack);
  const deliveryPackFocus = resolveDeliveryPackFocus(options.selectedPackages, { folderAgentPaths });
  const agentOperatingMode = resolveAgentOperatingMode(options.agentOperatingMode || report.recommendedAgentOperatingMode || DEFAULT_AGENT_OPERATING_MODE);
  const toolingRecommendations = buildToolingRecommendationBundle(report);
  const specializedContextPacks = SPECIALIZED_CONTEXT_PACK_PATHS.filter(path => deliveryPackFocus.generatedPaths.includes(path));

  const scoreJson: ScoreJsonExport = {
    scoreSchemaVersion: 2,
    product: 'ShipSeal',
    generatedBy: 'Generated by ShipSeal',
    repositoryName: report.repoName,
    scanTimestamp: report.scannedAt,
    source: report.source,
    score: report.score,
    status: report.level,
    isReady: report.isReady,
    criticalBlockers: report.blockers,
    improvements: report.improvements,
    categories: report.categories,
    legacyReadiness: {
      score: report.score,
      status: report.level,
      isReady: report.isReady,
      categories: report.categories,
    },
    repositoryHealth: report.repositoryHealth,
    detectedStack: report.stack,
    scanSummary: report.scanSummary,
    scanEvidence: report.scanEvidence,
    aiNarrative: report.aiNarrative,
    repoContextPack: {
      repositoryName: report.repoContextPack.repositoryName,
      detectedStack: report.repoContextPack.detectedStack,
      packageManager: report.repoContextPack.packageManager,
      scriptCount: Object.keys(report.repoContextPack.scripts).length,
      keyFolders: report.repoContextPack.keyFolders,
      existingInstructionFiles: report.repoContextPack.existingInstructionFiles,
      blockerCount: report.repoContextPack.blockers.length,
      ignoredFolders: report.repoContextPack.ignoredFolders,
      securityFindingCount: report.repoContextPack.securityFindings.length,
      mcpStatus: report.repoContextPack.mcpSummary.status,
      rawFileContentsIncluded: false,
    },
    generatedFiles: deliveryPackFocus.generatedPaths,
    outputCount: deliveryPackFocus.generatedPaths.length,
    agentOperatingMode: buildAgentOperatingModeSummary(agentOperatingMode),
    toolingRecommendations,
    specializedContextPacks: {
      generated: specializedContextPacks.length > 0,
      files: specializedContextPacks,
      outputCount: specializedContextPacks.length,
    },
    deliveryPackFocus: {
      selectedGoals: deliveryPackFocus.selectedGoals,
      emphasizedFiles: deliveryPackFocus.emphasizedPaths,
      generatedFiles: deliveryPackFocus.generatedPaths,
      outputCount: deliveryPackFocus.generatedPaths.length,
      manifestFiles: deliveryPackFocus.manifestPaths,
      manifestOutputCount: deliveryPackFocus.manifestPaths.length,
      readinessPrFiles: deliveryPackFocus.readinessPrPaths,
      readinessPrOutputCount: deliveryPackFocus.readinessPrPaths.length,
      fullPackage: deliveryPackFocus.fullPackage,
      packageLabel: deliveryPackFocus.packageLabel,
      packageSummary: deliveryPackFocus.packageSummary,
    },
    mcpReadiness: {
      score: report.mcpReadiness.score,
      status: report.mcpReadiness.status,
      summary: report.mcpReadiness.summary,
      aiNarrative: report.mcpReadiness.aiNarrative,
      recommendedServerCategories: report.mcpReadiness.recommendedServerCategories,
      riskFindings: report.mcpReadiness.riskFindings,
      generatedFiles: report.mcpReadiness.generatedFiles.map(file => file.filename),
    },
  };

  return stripUndefined(scoreJson);
}

export function downloadTextFile(filename: string, content: string, type = 'text/plain;charset=utf-8') {
  const blob = new Blob([content], { type });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  link.click();
  URL.revokeObjectURL(url);
}

export function downloadJsonFile(filename: string, value: unknown) {
  downloadTextFile(filename, `${JSON.stringify(value, null, 2)}\n`, 'application/json;charset=utf-8');
}

