function stripUndefined<T>(value: T): T {
  if (Array.isArray(value)) {
    return value.map(item => stripUndefined(item)) as T;
  }
  if (value && typeof value === 'object') {
    const cleaned: Record<string, unknown> = {};
    for (const [key, entry] of Object.entries(value as Record<string, unknown>)) {
      if (entry !== undefined) cleaned[key] = stripUndefined(entry);
    }
    return cleaned as T;
  }
  return value;
}

export async function buildAgentPackZipBlob(
  files: AgentPackFile[],
  mcpFiles: MCPPolicyFile[] = [],
  contextFiles?: { markdown: string; json: unknown },
  options: BuildAgentPackZipOptions = {}
): Promise<Blob> {
  const JSZip = (await import('jszip') as ZipModule).default;
  const zip = new JSZip();
  const deliveryFiles = buildDeliveryPackFiles({
    agentFiles: files,
    mcpFiles,
    contextFiles,
    repositoryName: options.repositoryName,
    scoreJson: options.scoreJson,
    intake: options.intake,
    selectedPackages: options.selectedPackages,
  });

  for (const file of deliveryFiles) {
    zip.file(file.path, file.content);
  }

  return zip.generateAsync({ type: 'blob' });
}

export async function downloadAgentPackZip(
  repositoryName: string,
  files: AgentPackFile[],
  mcpFiles: MCPPolicyFile[] = [],
  contextFiles?: { markdown: string; json: unknown },
  scoreJson?: unknown,
  intake?: PartialProjectIntake,
  selectedPackages?: string[]
) {
  const normalizedIntake = normalizeProjectIntake(intake, repositoryName);
  const blob = await buildAgentPackZipBlob(files, mcpFiles, contextFiles, { repositoryName, scoreJson, intake: normalizedIntake, selectedPackages });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = buildAgentPackZipFilename(normalizedIntake.projectName);
  link.click();
  URL.revokeObjectURL(url);
}
